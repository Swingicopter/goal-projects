function borderRadius(){
    let image = document.getElementById('poster')
    image.style.borderRadius = '15px'
}

function boxShadow(){
    let image = document.getElementById('poster')
    image.style.boxShadow = '5px 5px 10px 2px rgba(0, 0, 0, 0.75)'
}

function changePicture(){
    let image = document.getElementById('poster')
    image.src = 'monitor.png'
}